    public List<MeuCampo> gerarLista(){
        //if(lista.size()==0){
            JInternalFrame container = (JInternalFrame) getFocusCycleRootAncestor();
        
        for (int i = 0; i < container.getContentPane().getComponentCount(); i++) {
            //varre todos os componentes
            Component c = container.getContentPane().getComponent(i);
            if(c instanceof MeuCampo){
                //apaga os valores
                MeuCampo field = (MeuCampo) c;
                lista.add(field);
            }
        }
        //}
        return lista;
    }
    
    public void habilitarCampos(boolean status){
        for(MeuCampo m: gerarLista()){
            m.setEnabled(status);
        }
    }
    
    public void limparCampos(){
        for(MeuCampo m: gerarLista()){
            m.setText("");
        }
    }

