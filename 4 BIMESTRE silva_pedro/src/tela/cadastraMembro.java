    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        membro = daoMembro.buscaId(Integer.parseInt(id.getText()));
        nome.setText(membro.getNome());
        ano = membro.getDataNascimento().substring(0, 4);
        mes = membro.getDataNascimento().substring(5, 7);
        dia = membro.getDataNascimento().substring(8, 10);

        for (int i = 0; i < cidade.getItemCount() - 1; i++) {
            cidade.setSelectedIndex(i);
            if (membro.getCidade().equals(cidade.getItemAt(i))) {
                break;
            }
        }

        for (int j = 0; j < sexo.getItemCount() - 1; j++) {
            sexo.setSelectedIndex(j);
            if (membro.getSexo().equals(sexo.getItemAt(j))) {
                break;
            }
        }
        dataNascimento = dia + mes + ano;
        datanascv.setText(dataNascimento);
        cpf.setText(membro.getCpf());
        endereco.setText(membro.getEndereco());
        telefone.setText(membro.getTelefone());
        celular.setText(membro.getCelular());
        usuario.setText(membro.getUsuario());
        senha.setText(membro.getSenha());
    }//GEN-LAST:event_buscarActionPerformed
