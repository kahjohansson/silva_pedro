    private void cadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cadastrarMouseClicked
        Membro membro = new Membro();
        DAOMembro dao1 = new DAOMembro();
        
        membro.setNome(nome.getText());
        membro.setSexo(sexo.getSelectedItem().toString());
        dia = datanascv.getText().substring(0, 2);
        mes = datanascv.getText().substring(3, 5);
        ano = datanascv.getText().substring(6);
        membro.setDataNascimento(ano + "-" + mes + "-" + dia);

        membro.setCpf(cpf.getText());
        membro.setEndereco(endereco.getText());
        membro.setCidade(cidade.getSelectedItem().toString());
        membro.setTelefone(telefone.getText());
        membro.setCelular(celular.getText());
        membro.setUsuario(usuario.getText());
        membro.setSenha(senha.getText());
        dao1.salvar(membro);
        JOptionPane.showMessageDialog(null, "Cadastro concluído com sucesso!");
        dispose();
        
    }//GEN-LAST:event_cadastrarMouseClicked

